package commonlibrary.math;

/**
 * @author lfj  2015年8月21日
 * @Description: Math的各种方法
 */
public class demo {
	public static void main(String[] args) {
		//
		System.out.println("求9.0的平方根："+Math.sqrt(9.0));
		System.out.println("取两数的最大值："+Math.max(10, 12));
		System.out.println("取两数的最小值："+Math.min(10, 12));
		System.out.println("求2的3次方："+Math.pow(2, 3));
		System.out.println("33.6四舍五入："+Math.round(33.6));
		System.out.println("33.6舍："+Math.floor(33.6));
		System.out.println("33.6入："+Math.ceil(33.6));
	}
}
